/**
 * Get all IANA timezones formatted as picker items.
 * Uses Intl.supportedValuesOf when available, falls back to a comprehensive list.
 */
export function getTimezones(): { code: string; label: string }[] {
  let zones: string[];

  try {
    zones = (Intl as any).supportedValuesOf('timeZone');
  } catch {
    // Fallback for engines that don't support supportedValuesOf
    zones = FALLBACK_TIMEZONES;
  }

  return zones.map((tz) => {
    // Format: "America/New_York" -> "America/New York"
    const label = tz.replace(/_/g, ' ');
    return { code: tz, label };
  });
}

